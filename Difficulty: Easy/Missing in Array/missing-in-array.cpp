class Solution {
  public:
    int missingNumber(vector<int>& arr) {
        // code here
        
        int xor1=0;
        int n = arr.size();
        
        for(int i=0; i<n; i++) {
            xor1 ^= arr[i];
            xor1 ^= (i + 1); 
        }
        
        xor1 ^= (n + 1);
        
        return xor1;
    }
    
};

